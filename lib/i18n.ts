const STORAGE_KEY = "who-i-am-locale";

i18n.use(initReactI18next).init({
  resources,
  lng: "ru",
  fallbackLng: "ru",
  interpolation: {
    escapeValue: false,
  },
});

// Сохранение выбранного языка и синхронизация с <html lang>. Смена языка по localStorage
// выполняется в AppContent после гидрации, чтобы не ломать SSR (сервер и первый клиентский
// рендер должны совпадать — оба с lng: "ru").
if (typeof window !== "undefined") {
  i18n.on("languageChanged", (lng) => {
    localStorage.setItem(STORAGE_KEY, lng);
    document.documentElement.lang = lng.startsWith("en") ? "en" : "ru";
  });
}

export default i18n;
